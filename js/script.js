    function lerp(a, b, t) { return a + (b - a) * t; }
    function lerpColor(c1, c2, t) {
      return [lerp(c1[0], c2[0], t), lerp(c1[1], c2[1], t), lerp(c1[2], c2[2], t)];
    }

    let currentBg = [12, 15, 10];
    let targetBg = [12, 15, 10];
    let bgRaf = null;

    function updateSectionColorWash(y, vh) {
      const isLight = root.hasAttribute('data-theme');
      const page = document.getElementById('mainPage');
      const base = isLight ? [252, 246, 232] : [12, 15, 10];

      let bestInfluence = 0;
      let bestColor = base;

      for (let i = 0; i < sectionColors.length; i++) {
        const sec = document.getElementById(sectionColors[i].id);
        if (!sec) continue;
        const rect = sec.getBoundingClientRect();
        const center = rect.top + rect.height / 2;
        const dist = Math.abs(center - vh / 2);

        /* wider influence window = smoother crossfade between sections */
        const influence = Math.max(0, 1 - dist / (vh * 0.9));

        if (influence > bestInfluence) {
          bestInfluence = influence;
          bestColor = sectionColors[i][isLight ? 'light' : 'dark'];
        }
      }

      targetBg = bestInfluence > 0
        ? lerpColor(base, bestColor, Math.min(bestInfluence, 1))
        : base;

      /* smooth interpolation toward target — no abrupt jump */
      if (!bgRaf) animateBg(page);
    }

    function animateBg(page) {
      const speed = bgRaf ? 0.07 : 0.12;/* 0.04 = very slow, 0.12 = snappier */
      const r = lerp(currentBg[0], targetBg[0], speed);
      const g = lerp(currentBg[1], targetBg[1], speed);
      const b = lerp(currentBg[2], targetBg[2], speed);
      currentBg = [r, g, b];

      page.style.backgroundColor =
        `rgb(${Math.round(r)},${Math.round(g)},${Math.round(b)})`;

      const dist = Math.abs(r - targetBg[0]) + Math.abs(g - targetBg[1]) + Math.abs(b - targetBg[2]);
      if (dist > 0.4) {
        bgRaf = requestAnimationFrame(() => animateBg(page));
      } else {
        currentBg = [...targetBg];
        page.style.backgroundColor =
          `rgb(${Math.round(targetBg[0])},${Math.round(targetBg[1])},${Math.round(targetBg[2])})`;
        bgRaf = null;
      }
    }

